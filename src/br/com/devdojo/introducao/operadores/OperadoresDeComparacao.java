package br.com.devdojo.introducao.operadores;

public class OperadoresDeComparacao {
    /*Operadores Comparação
        < (Menor que)
            > (Maior que)
            <= (Menor Igual)
            >= (Maior Igual)
            == (Igual)
            != (Diferente)
    Obs: o resultado de qualquer operação utilizando um desses operadores
    sempre irá retornar um Boolean.*/

    public static void main(String[] args){
        boolean dezMaiorQueVinte = 10 >= 20;
        boolean dezMenorIgualVinte = 10 <= 20;
        System.out.println(dezMaiorQueVinte);
        System.out.println(dezMenorIgualVinte);
    }
}
